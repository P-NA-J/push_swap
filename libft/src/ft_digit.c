#include "../includes/libft.h"

int			ft_digit(long nb)
{
	int				i;

	i = 1;
	if (nb < 0)
	{
		i += 1;
		return (i + ft_digit(-(nb / 10)));
	}
	if ((nb / 10) > 0)
		return (1 + ft_digit(nb / 10));
	return (1);
}
